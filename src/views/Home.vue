        msg:'',
        create_date:'',

        comments:[]
    }
  },
  mounted() {
         window.kakao && window.kakao.maps ? this.initMap() : this.addScript(); 
    }, 
    methods : { 
        textCopy(){
          var textToCopy = document.getElementById('addrVal');
          textToCopy.select();
          textToCopy.setSelectionRange(0, 9999); 
          document.execCommand("copy");
          
          alert('주소가 복사 되었습니다.')
        },
        initMap() { 
          var container = document.getElementById('map'); 
          var options = { center: new kakao.maps.LatLng(37.535344, 127.095805), level: 4 }; 
          var map = new kakao.maps.Map(container, options); 
          //마커추가하려면 객체를 아래와 같이 하나 만든다. 
          var marker = new kakao.maps.Marker({ 
              position: map.getCenter() 
          }); 
          marker.setMap(map); 

          var zoomControl = new kakao.maps.ZoomControl();
          map.addControl(zoomControl, kakao.maps.ControlPosition.RIGHT);
        }, 
        addScript() { 
          const script = document.createElement('script');
          /* global kakao */ 
          script.onload = () => kakao.maps.load(this.initMap); 
          script.src = '//dapi.kakao.com/v2/maps/sdk.js?autoload=false&appkey=fa26ac4bdca6a8631a704dabdd184bc8'; 
          document.head.appendChild(script); 
        } ,
        submit(){
          if(this.name == '' || this.msg == ''){
                  alert('빈칸을 가득 채워주세요 ๑•‿•๑')
              }
          else{
            const today = new Date();
            const date = today.getFullYear()+'-'+(today.getMonth()+1)+'-'+today.getDate();
            const time = today.getHours() + ":" + today.getMinutes() + ":" + today.getSeconds();
            const dateTime = date +' '+ time;
            this.create_date = dateTime;

            const comment = {
                name: this.name,
                msg: this.msg,
                create_date: this.create_date
            }

            db.collection('comments').add(comment).then(() => {
                console.log('added to db');
                this.name = '';
                this.msg = '';
            })
            // this.$router.go($router.currentRoute);
            // this.$router.replace('/');
          }
            
        },
        close(){
            this.showImgModal = false;
            console.log('close', close);
        }
      },
      created(){
        db.collection('comments').onSnapshot( res => {
            const changes = res.docChanges()

            changes.forEach(change => {
                if(change.type === 'added'){
                    this.comments.push({
                        ...change.doc.data(),
                        id: change.doc.id
                    })
                }
            })
        })
        // this.$router.replace('/');
    }

}
